// Afficher les nombres de 1 - 10
// for (initialisation; condition de sortie; opération après chaque itération)
for (var i = 1; i <= 10; i = i + 1) {

	console.log('Je suis dans la boucle');

}

console.log('====================');

// 2ème ex. 0, 1, 2, 3, 4
for (var i = 0; i <= 4; i = i + 1) {
	console.log(i);
}

console.log('====================');

for (var j = 10; j > 5; j = j - 1) {
	console.log(j);
}

